'''
Write a python program to read multiple lines of text input and store the input into a string.
'''

def main():
    '''
    main function
    '''
    a_a = ""
    n_n = int(input())
    for i_i in range(n_n):
        del i_i
        a_a = input()
        print(a_a)

if __name__ == '__main__':
    main()
